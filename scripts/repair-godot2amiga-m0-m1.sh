#!/usr/bin/env bash
set -euo pipefail

# Godot2Amiga repository repair/bootstrap script
#
# Run from the repository root:
#   chmod +x scripts/repair-m0-m1.sh
#   ./scripts/repair-m0-m1.sh
#
# Optional:
#   GODOT_BIN=godot4 ./scripts/repair-m0-m1.sh
#
# This script:
# - backs up the old exporter directory when present
# - creates an installable Godot plugin under addons/godot2amiga
# - creates a minimal example Godot project
# - generates CONTRIBUTING.md and CODE_OF_CONDUCT.md
# - creates repository validation scripts
# - replaces CI with static checks plus an optional Godot headless parse test
#
# It deliberately implements the first M1 vertical slice as an editor tool:
# "Tools -> Godot2Amiga -> Export ACE C skeleton".
# A custom EditorExportPlatformExtension can be introduced after the generator
# and its tests are stable.

readonly SCRIPT_VERSION="1.0.0"
readonly REPO_ROOT="$(pwd)"
readonly TIMESTAMP="$(date +%Y%m%d-%H%M%S)"
readonly BACKUP_ROOT="${REPO_ROOT}/.repair-backup/${TIMESTAMP}"

log() {
    printf '[Godot2Amiga] %s\n' "$*"
}

die() {
    printf '[Godot2Amiga] ERROR: %s\n' "$*" >&2
    exit 1
}

require_repo_root() {
    [[ -f "${REPO_ROOT}/README.md" ]] || die "Run this script from the repository root (README.md was not found)."

    if [[ -d "${REPO_ROOT}/.git" ]]; then
        log "Git repository detected."
    else
        log "Warning: .git was not found; continuing anyway."
    fi
}

backup_existing_files() {
    mkdir -p "${BACKUP_ROOT}"

    local paths=(
        "exporter"
        "addons/godot2amiga"
        "examples/hello-amiga"
        "CONTRIBUTING.md"
        "CODE_OF_CONDUCT.md"
        ".github/workflows/ci.yml"
        "scripts/validate-repository.sh"
        "scripts/test-godot-plugin.sh"
    )

    local copied=0
    for relative_path in "${paths[@]}"; do
        if [[ -e "${REPO_ROOT}/${relative_path}" ]]; then
            mkdir -p "${BACKUP_ROOT}/$(dirname "${relative_path}")"
            cp -a "${REPO_ROOT}/${relative_path}" "${BACKUP_ROOT}/${relative_path}"
            copied=1
        fi
    done

    if [[ "${copied}" -eq 1 ]]; then
        log "Existing files backed up to .repair-backup/${TIMESTAMP}/"
    else
        rmdir "${BACKUP_ROOT}" 2>/dev/null || true
        rmdir "${REPO_ROOT}/.repair-backup" 2>/dev/null || true
        log "No existing repair-target files needed backup."
    fi
}

create_directories() {
    mkdir -p \
        addons/godot2amiga \
        examples/hello-amiga \
        scripts \
        .github/workflows
}

write_plugin_cfg() {
    cat > addons/godot2amiga/plugin.cfg <<'EOF'
[plugin]

name="Godot2Amiga"
description="Exports a constrained Godot 2D project to an ACE-compatible Amiga C project skeleton."
author="Godot2Amiga contributors"
version="0.1.0"
script="plugin.gd"
EOF
}

write_plugin_script() {
    cat > addons/godot2amiga/plugin.gd <<'GDSCRIPT'
@tool
extends EditorPlugin

const MENU_EXPORT := "Godot2Amiga/Export ACE C skeleton"
const DEFAULT_OUTPUT_DIRECTORY := "res://build/amiga"

var _generator: RefCounted


func _enter_tree() -> void:
    _generator = preload("res://addons/godot2amiga/project_generator.gd").new()
    add_tool_menu_item(MENU_EXPORT, _export_project)


func _exit_tree() -> void:
    remove_tool_menu_item(MENU_EXPORT)
    _generator = null


func _export_project() -> void:
    if _generator == null:
        push_error("Godot2Amiga generator is not initialized.")
        return

    var error: Error = _generator.generate(DEFAULT_OUTPUT_DIRECTORY)
    if error != OK:
        push_error(
            "Godot2Amiga export failed with error %d. See the editor output for details."
            % error
        )
        return

    print(
        "Godot2Amiga: generated ACE C skeleton in %s"
        % ProjectSettings.globalize_path(DEFAULT_OUTPUT_DIRECTORY)
    )
    EditorInterface.get_resource_filesystem().scan()
GDSCRIPT

    cat > addons/godot2amiga/project_generator.gd <<'GDSCRIPT'
@tool
extends RefCounted

const GENERATED_FILES := {
    "main.c": """/*
 * Generated by Godot2Amiga.
 *
 * This is an M1 project skeleton, not yet a complete scene conversion.
 */

#include <stdio.h>

int main(void) {
    puts("Hello from Godot2Amiga!");
    return 0;
}
""",
    "Makefile": """CC ?= m68k-amigaos-gcc
CFLAGS ?= -O2 -Wall -Wextra
TARGET ?= godot2amiga-demo

.PHONY: all clean

all: $(TARGET)

$(TARGET): main.c
\t$(CC) $(CFLAGS) -o $@ $<

clean:
\trm -f $(TARGET)
""",
    "README_BUILD.txt": """Godot2Amiga generated project skeleton
=======================================

Generated files:
- main.c
- Makefile
- project_manifest.json

The default Makefile expects an m68k-amigaos-gcc cross compiler.

This M1 skeleton proves that the Godot editor plugin can generate deterministic
native-project output. It does not yet translate Godot scenes or link ACE.
"""
}


func generate(output_directory: String) -> Error:
    var directory_error := DirAccess.make_dir_recursive_absolute(
        ProjectSettings.globalize_path(output_directory)
    )
    if directory_error != OK:
        push_error(
            "Godot2Amiga: could not create output directory '%s' (error %d)."
            % [output_directory, directory_error]
        )
        return directory_error

    for filename: String in GENERATED_FILES:
        var write_error := _write_text_file(
            output_directory.path_join(filename),
            GENERATED_FILES[filename]
        )
        if write_error != OK:
            return write_error

    var manifest := {
        "format": "godot2amiga-project-manifest",
        "format_version": 1,
        "generator_version": "0.1.0",
        "godot_project_name": str(
            ProjectSettings.get_setting("application/config/name", "Unnamed project")
        ),
        "main_scene": str(
            ProjectSettings.get_setting("application/run/main_scene", "")
        ),
        "target": {
            "platform": "amiga",
            "chipset": "ocs",
            "runtime": "ace",
            "video_standard": "pal"
        }
    }

    var manifest_error := _write_text_file(
        output_directory.path_join("project_manifest.json"),
        JSON.stringify(manifest, "\t") + "\n"
    )
    if manifest_error != OK:
        return manifest_error

    return OK


func _write_text_file(path: String, contents: String) -> Error:
    var file := FileAccess.open(path, FileAccess.WRITE)
    if file == null:
        var open_error := FileAccess.get_open_error()
        push_error(
            "Godot2Amiga: could not open '%s' for writing (error %d)."
            % [path, open_error]
        )
        return open_error

    file.store_string(contents)
    file.close()
    return OK
GDSCRIPT
}

write_example_project() {
    cat > examples/hello-amiga/project.godot <<'EOF'
; Engine configuration file.
; Minimal project used to verify that the Godot2Amiga plugin parses and loads.

config_version=5

[application]

config/name="Godot2Amiga Hello"
run/main_scene="res://main.tscn"

[display]

window/size/viewport_width=320
window/size/viewport_height=256
window/size/window_width_override=640
window/size/window_height_override=512
window/stretch/mode="canvas_items"

[editor_plugins]

enabled=PackedStringArray("res://addons/godot2amiga/plugin.cfg")

[rendering]

renderer/rendering_method="gl_compatibility"
renderer/rendering_method.mobile="gl_compatibility"
EOF

    cat > examples/hello-amiga/main.tscn <<'EOF'
[gd_scene format=3]

[node name="HelloAmiga" type="Node2D"]
EOF

    # Keep the example self-contained using a symlink where supported.
    rm -rf examples/hello-amiga/addons
    mkdir -p examples/hello-amiga/addons

    if ln -s ../../../addons/godot2amiga examples/hello-amiga/addons/godot2amiga 2>/dev/null; then
        log "Created example-project plugin symlink."
    else
        cp -a addons/godot2amiga examples/hello-amiga/addons/godot2amiga
        log "Symlinks unavailable; copied plugin into the example project."
    fi
}

write_contributing() {
    cat > CONTRIBUTING.md <<'EOF'
# Contributing to Godot2Amiga

Thank you for helping build a modern Godot-to-Amiga development pipeline.

## Project status

Godot2Amiga is experimental. The current milestone establishes a valid,
installable Godot editor plugin and a deterministic ACE-oriented C project
skeleton. Scene conversion, ACE integration, asset conversion, compilation,
packaging, and emulator execution are later milestones.

## Before opening a pull request

1. Read `README.md`, `docs/architecture.md`, `docs/non-goals.md`, and
   `docs/target-hardware.md`.
2. Keep changes aligned with the current roadmap milestone.
3. Run:

   ```bash
   ./scripts/validate-repository.sh
   ```

4. When Godot 4 is installed, also run:

   ```bash
   ./scripts/test-godot-plugin.sh
   ```

## Development principles

- Godot is the authoring front end, not the Amiga runtime.
- Generated code must target native 68k Amiga software.
- OCS/PAL and ACE are the initial reference target.
- Prefer deterministic generated output.
- Fail clearly when a Godot feature cannot be represented.
- Avoid silently producing incorrect Amiga output.

## Pull requests

Keep pull requests focused. Describe:

- the problem being solved;
- the milestone or issue it belongs to;
- how it was tested;
- any generated-format or compatibility implications.

Do not commit generated `build/` output unless a test fixture explicitly
requires it.

## Reporting bugs

Include the Godot version, operating system, reproduction steps, expected
result, actual result, and relevant editor or CI output.

## Licensing

By contributing, you agree that your contribution is licensed under the
repository's existing license.
EOF
}

