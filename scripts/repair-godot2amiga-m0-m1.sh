write_validation_scripts() {
    cat > scripts/validate-repository.sh <<'EOF'
#!/usr/bin/env bash
set -euo pipefail

fail() {
    printf 'ERROR: %s\n' "$*" >&2
    exit 1
}

required_files=(
    "README.md"
    "LICENSE"
    "CONTRIBUTING.md"
    "CODE_OF_CONDUCT.md"
    "addons/godot2amiga/plugin.cfg"
    "addons/godot2amiga/plugin.gd"
    "addons/godot2amiga/project_generator.gd"
    "examples/hello-amiga/project.godot"
    "examples/hello-amiga/main.tscn"
)

for file in "${required_files[@]}"; do
    [[ -f "${file}" ]] || fail "Missing required file: ${file}"
done

if grep -RIn --include='*.gd' '```' addons examples; then
    fail "Markdown code fences were found inside GDScript files."
fi

grep -q '^script="plugin.gd"$' addons/godot2amiga/plugin.cfg \
    || fail "plugin.cfg does not point to plugin.gd."

grep -q '^@tool$' addons/godot2amiga/plugin.gd \
    || fail "plugin.gd is not marked as an editor tool."

grep -q 'extends EditorPlugin' addons/godot2amiga/plugin.gd \
    || fail "plugin.gd does not extend EditorPlugin."

printf 'Repository structure and static checks passed.\n'
EOF

    cat > scripts/test-godot-plugin.sh <<'EOF'
#!/usr/bin/env bash
set -euo pipefail

find_godot() {
    if [[ -n "${GODOT_BIN:-}" ]] && command -v "${GODOT_BIN}" >/dev/null 2>&1; then
        command -v "${GODOT_BIN}"
        return
    fi

    local candidate
    for candidate in godot4 godot godot4.6 godot4.5 godot4.4; do
        if command -v "${candidate}" >/dev/null 2>&1; then
            command -v "${candidate}"
            return
        fi
    done

    return 1
}

GODOT="$(find_godot)" || {
    printf 'Godot 4 was not found. Set GODOT_BIN=/path/to/godot and retry.\n' >&2
    exit 2
}

printf 'Using Godot: %s\n' "${GODOT}"
"${GODOT}" --headless --editor --path examples/hello-amiga --quit-after 3

printf 'Godot headless plugin-load test passed.\n'
EOF

    chmod +x scripts/validate-repository.sh scripts/test-godot-plugin.sh
}

write_ci() {
    cat > .github/workflows/ci.yml <<'EOF'
name: CI

on:
  push:
  pull_request:

permissions:
  contents: read

jobs:
  repository-validation:
    name: Repository validation
    runs-on: ubuntu-latest

    steps:
      - name: Check out repository
        uses: actions/checkout@v4

      - name: Run static repository checks
        run: ./scripts/validate-repository.sh

  godot-plugin-parse:
    name: Godot plugin parse
    runs-on: ubuntu-latest

    steps:
      - name: Check out repository
        uses: actions/checkout@v4

      - name: Download Godot 4.4.1
        run: |
          set -euo pipefail
          curl --fail --location --retry 3 \
            --output /tmp/godot.zip \
            https://github.com/godotengine/godot/releases/download/4.4.1-stable/Godot_v4.4.1-stable_linux.x86_64.zip
          unzip -q /tmp/godot.zip -d /tmp/godot
          chmod +x /tmp/godot/Godot_v4.4.1-stable_linux.x86_64

      - name: Parse and load the example plugin
        env:
          GODOT_BIN: /tmp/godot/Godot_v4.4.1-stable_linux.x86_64
        run: ./scripts/test-godot-plugin.sh
EOF
}

write_exporter_notice() {
    mkdir -p exporter
    cat > exporter/README.md <<'EOF'
# Legacy exporter location

The installable Godot editor plugin now lives in:

```text
addons/godot2amiga/
```

The `exporter/` directory is reserved for future standalone generators,
templates, and compiler-facing components that are not themselves installed as
a Godot editor plugin.

Any previous contents were preserved by `scripts/repair-m0-m1.sh` under
`.repair-backup/<timestamp>/`.
EOF
}

run_checks() {
    ./scripts/validate-repository.sh

    if command -v "${GODOT_BIN:-godot4}" >/dev/null 2>&1 \
        || command -v godot >/dev/null 2>&1; then
        ./scripts/test-godot-plugin.sh
    else
        log "Godot was not found locally; skipped the headless load test."
        log "CI will run the Godot parse/load test."
    fi
}

show_git_summary() {
    if [[ -d .git ]]; then
        printf '\n'
        git status --short || true
    fi
}

main() {
    log "Godot2Amiga repair script ${SCRIPT_VERSION}"
    require_repo_root
    backup_existing_files
    create_directories
    write_plugin_cfg
    write_plugin_script
    write_example_project
    write_contributing
    write_code_of_conduct
    write_validation_scripts
    write_ci
    write_exporter_notice
    run_checks
    show_git_summary

    printf '\n'
    log "Repair complete."
    log "Open examples/hello-amiga in Godot 4, enable the plugin if necessary,"
    log "then use Tools -> Godot2Amiga -> Export ACE C skeleton."
    log "Suggested commit:"
    log "  git add ."
    log "  git commit -m \"fix: make the Godot exporter plugin installable and loadable\""
}

main "$@"
